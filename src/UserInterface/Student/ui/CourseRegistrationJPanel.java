    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
         int selectedRow = tblCourse.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showConfirmDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
          Course result = (Course) tblCourse.getValueAt(selectedRow, 1);
          student.addCourse(result.getCRN());
          result.addStudents(student);
          JOptionPane.showMessageDialog(null, "Course added successfully!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void btnUnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnRegisterActionPerformed
          int selectedRow = tblCourse.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showConfirmDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
          Course result = (Course) tblCourse.getValueAt(selectedRow, 1);
          student.removeCourse(result.getCRN());
          result.removeStudents(student);
          JOptionPane.showMessageDialog(null, "Course has been removed!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnUnRegisterActionPerformed

    private void populateCourses() {
        DefaultTableModel dtm = (DefaultTableModel) tblCourse.getModel();
        dtm.setRowCount(0);
        List<Course> allCourses = professorDirectory.getProfessorList().stream()
                .flatMap(professor -> professor.getCourseHistory().getCourseList().stream())
                .collect(Collectors.toList());
        for(Course course : allCourses){
            Object[] row = new Object[8];
            row[0] = course.getCRN();
            row[1] = course;
            row[2] = course.getStartDate();
            row[3] = course.getEndDate();
            row[4] = course.getDay();
            row[5] = course.getTime();
            row[6] = course.getProfName();
            row[7] = course.getProfRepo();
            
            dtm.addRow(row);
        } 
    }


