    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        //Validation part

       // int crn = Integer.parseInt(txtSearchCrn.getText());
            List<Course> allCourses = professorDirectory.getProfessorList().stream()
                .flatMap(professor -> professor.getCourseHistory().getCourseList().stream())
                    .filter(course -> (cbLanguage.getSelectedItem().equals(course.getLanguage())) || (cbRegion.getSelectedItem().equals(course.getRegion())) || (txtCourseName.getText().equals(course.getCourseName())) || (course.getProfName().equals(txtProfName.getText())) )
                .collect(Collectors.toList());
        if(allCourses.isEmpty()){
            JOptionPane.showMessageDialog(null, "Courses not found!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
              DefaultTableModel dtm = (DefaultTableModel) tblCourse.getModel();
        dtm.setRowCount(0);
        
            for(Course course : allCourses){
            Object[] row = new Object[6];
            row[0] = course.getCRN();
            row[1] = course;
            row[2] = course.getStartDate();
            row[3] = course.getEndDate();
            row[4] = course.getDay();
            row[5] = course.getTime();
            
            dtm.addRow(row);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed
    }
